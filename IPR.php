        <h3>Types of IPRs</h3>
        <ul class="pt-5">
            <li>Patent: Protects Inventions e.g., mobile phone technology, mechanical devices</li>
            <li>Copyright: Protects expression of Ideas in tangible form e.g., books, music, software </li>
            <li>Trademark: Protects indication of source of goods or services e.g., Pepsi, IBM </li>
            <li>Design: Protects outer appearance of a trade article e.g., car designs, phone designs </li>
            <li>Geographical Indication: Protects geographic source of goods e.g., Orissa Pattachitra, Orissa </li>
            <li>Trade Secrets: Protects know-how, non-patentable inventions e.g., Coca-Cola Recipe</li>
        </ul>
