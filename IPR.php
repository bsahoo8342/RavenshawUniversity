            exploiting the full potential of IP for social, economic and cultural development. A step in this direction
            has been taken by instituting a Patent Information Centre (PIC) under the aegis of the Odisha State Council
            on Science & Technology Department by Government of Odisha that facilitated the establishment of
            Intellectual Propperty Right Cells at different State funded Universities. Ravenshaw University Intellectual
            Property Rights Cell (RUIPR Cell) was established on 15th November, 2018 with a vision to develop human
            resource in the management of Intellectual Property Rights as an integral part of the innovation process and
            the innovation value chain in the University. Several patent, copyright and industrial design applications
            have been filed based on the innovations done at Ravenshaw University. The Cell is gradually building IPR
            consciousness and organizing several IPR awareness and implementation programmes in the University Campus. A
            key objective of this Cell is to integrate IPR with the education process to enhance the quality of
            thinking, propagate a culture of regard and respect for IPR and ensure speedy and accurate identification
            and protection of innovations arising out of the research work carried out in the University. Also, the Cell
            shall focus on bridging the gaps between the industry and the academia for technology sharing and mutual
            research.
        </p>

        <p style="text-align: justify;font-size: 1.3rem;">The term intellectual property reflects the idea that this
            subject matter is the product of the mind or the
            intellect, and that Intellectual Property rights may be protected at law in the same way as any other form
            of property. Intellectual property laws vary from jurisdiction to jurisdiction, such that the acquisition,
            registration or enforcement of IP rights must be pursued or obtained separately in each territory of
            interest. Intellectual property rights (IPR) can be defined as the rights given to people over the creation
            of their minds. They usually give the creator an exclusive right over the use of his/her creations for a
            certain period of time.
        </p>
