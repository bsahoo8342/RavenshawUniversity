                electronic media formats. A modest beginning in this direction has been put into place over the last six
                years. This includes setting up of Ravenshaw Knowledge Center (RKC) providing innovative services like
                Ravenshaw Knowledge Portal, Internet-cum-Digital Library, On-line Reading Room, UGC INFLIB Network,
                National Knowledge Network, subscription to subject databases, online journals and e resources. Besides,
                a music library named after the noted Oriya singer and lyricist, Akshaya Mohanty and a Braille reading
                system equipped with sophisticated scanner and printer facilities, exclusively devoted to enhance the
                reading experience of the visually impaired students, have also been set up. The other major focus of
                library innovation has been the aspect of digitalization of the old and rare texts of the library and
                other printed resources including century old institutional documents of Ravenshaw. This drive has
                entailed an elaborate process of preservation, selection and digitalization of the old and brittle
                printed resources of rare category, bundled up and dumped in gunny bags.</p>
