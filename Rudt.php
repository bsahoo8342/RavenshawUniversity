        <p style="text-align:justify;">
            Ravenshaw University Development Trust was born as a group called ‘Friends of Ravenshaw’ comprising a
            few former students of Ravenshaw. It held its first meeting in the Botanical Garden of the College on 7
            May 1994. The group met again on 18 June 1994 to mobilise a campaign for a ‘Deemed-to-be-University’
            status for the College. A Trust named Ravenshaw College Development Trust led by Amiya Bhusan Tripathy
            as the Chairman was formed on 11 July 1994 to carry forward the campaign. The Trust prepared a
            blue-print called “Vision-2010” to convert Ravenshaw College to a University of the new millennium.
            Eventually, in September 2004, the UGC accorded the status of ‘College with Potential for Excellence’ to
            Ravenshaw College. Eventually, on 15 November 2006, the College was elevated to the status of a
            full-fledged University and came to be known as Ravenshaw University.
            <br><br>
            The Trust, which played a major role in the upgradation of Ravenshaw College to a unitary university,
            has raised more than Rs. 30 lakh which has been used to institute Teaching-cum-Research Fellowships for
            the Ph. D. students of the University.
